function capitalizeFirstLetter(string) {
    return string.charAt(0).toUpperCase() + string.slice(1);
}

$(document).ready(function() {

    $(".productColor").each(function(){
        let colorCode = $(this).next("input[type='hidden']").val()

        $(this).append('<option value="">Ürün İle Eşleşen Rengi Seçin</option>');
        for(let i = 0;i<ozonColors.length;i++){
            let selected = ""
            if(ozonColors[i].id == colorCode)
                selected = "selected"
            $(this).append('<option '+selected+' value="' + ozonColors[i].id + '">' + capitalizeFirstLetter(ozonColors[i].value) + '</option>');
        }
    });

});